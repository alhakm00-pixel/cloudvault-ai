import toast from 'react-hot-toast'

interface Segment { index: number; start: number; end: number; text: string; textAr?: string }

interface AIResult {
  segments?:     Segment[]
  summary?:      string
  keyPoints?:    string[]
  topics?:       string[]
  hasTranslation?: boolean
  detectedLanguage?: string
  duration?:     number
  segmentCount?: number
}

interface AIPanelProps {
  fileId:    string
  fileName:  string
  mimeType:  string
  onSeek?:   (seconds: number) => void  // jump to timestamp in player
}

type Tab = 'subtitles' | 'summary' | 'search' | 'chat'

function formatTime(s: number) {
  const h = Math.floor(s / 3600)
  const m = Math.floor((s % 3600) / 60)
  const sec = Math.floor(s % 60)
  return h > 0
    ? `${h}:${String(m).padStart(2,'0')}:${String(sec).padStart(2,'0')}`
    : `${m}:${String(sec).padStart(2,'0')}`
}

