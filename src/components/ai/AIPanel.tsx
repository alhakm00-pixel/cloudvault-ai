// ─── Search Tab ───────────────────────────────────────────────────────────────
function SearchTab({ fileId, onSeek }: { fileId: string; onSeek?: (s: number) => void }) {
  const [query, setQuery]     = useState('')
  const [loading, setLoading] = useState(false)
  const [results, setResults] = useState<Array<{ start: number; end: number; text: string; textAr?: string }>>([])
  const [searched, setSearched] = useState(false)

  const doSearch = async () => {
    if (!query.trim()) return
    setLoading(true)
    try {
      await new Promise(r => setTimeout(r, 600))
      // Demo results
      const demo = [
        { start: 9.2,  end: 15.0, text: 'CloudVault AI stores your files safely.',    textAr: 'يحفظ CloudVault AI ملفاتك بأمان تام.' },
        { start: 20.8, end: 27.3, text: 'Smart search works inside video content.',   textAr: 'يعمل البحث الذكي داخل محتوى الفيديو.' },
      ].filter(r => r.text.toLowerCase().includes(query.toLowerCase()) || (r.textAr ?? '').includes(query))
      setResults(demo)
      setSearched(true)
    } finally { setLoading(false) }
  }

  return (
    <div className="space-y-4">
      <div className="flex gap-2">
        <div className="relative flex-1">
          <Search className="absolute right-3 top-1/2 -translate-y-1/2 w-4 h-4 text-white/30" />
          <input
            value={query}
            onChange={e => setQuery(e.target.value)}
            onKeyDown={e => e.key === 'Enter' && doSearch()}
            placeholder="ابحث في محتوى الملف..."
            className="input-glass pr-10 text-sm"
          />
        </div>
        <motion.button
          whileTap={{ scale: 0.95 }}
          onClick={doSearch} disabled={loading || !query.trim()}
          className="btn-primary px-4 py-2 text-sm disabled:opacity-50 flex items-center gap-1.5"
        >
          {loading ? <Loader2 className="w-4 h-4 animate-spin" /> : <Search className="w-4 h-4" />}
        </motion.button>
      </div>

      {searched && results.length === 0 && (
        <div className="text-center py-8">
          <p className="text-white/40 text-sm">لا توجد نتائج لـ &quot;{query}&quot;</p>
        </div>
      )}

      {results.length > 0 && (
        <div className="space-y-2">
          <p className="text-xs text-white/40">{results.length} نتيجة</p>
          {results.map((r, i) => (
            <motion.div
              key={i}
              initial={{ opacity: 0, y: 6 }}
              animate={{ opacity: 1, y: 0 }}
              transition={{ delay: i * 0.05 }}
              onClick={() => onSeek?.(r.start)}
              className="glass rounded-xl p-3 cursor-pointer hover:bg-white/8 transition-all"
            >
              <div className="flex items-center gap-2 mb-1.5">
                <button className="flex items-center gap-1 text-xs text-royal-400 font-mono bg-royal-500/10 px-2 py-0.5 rounded-lg">
                  <Clock className="w-3 h-3" />{formatTime(r.start)}
                </button>
                <span className="text-xs text-white/30">→</span>
                <span className="text-xs text-white/30 font-mono">{formatTime(r.end)}</span>
              </div>
              <p className="text-sm text-white/80 leading-relaxed">{r.text}</p>
              {r.textAr && <p className="text-xs text-royal-300 mt-1">{r.textAr}</p>}
            </motion.div>
          ))}
        </div>
      )}

      {!searched && (
        <div className="text-center py-8">
          <Search className="w-10 h-10 text-white/10 mx-auto mb-2" />
          <p className="text-white/30 text-sm">اكتب كلمة للبحث في محتوى الملف</p>
          <p className="text-white/20 text-xs mt-1">يدعم البحث العربي والإنجليزي</p>
        </div>
      )}
    </div>
  )
}

// ─── Chat Tab ─────────────────────────────────────────────────────────────────
function ChatTab({ fileId, fileName }: { fileId: string; fileName: string }) {
  const [messages, setMessages] = useState<{ role: 'user' | 'ai'; text: string }[]>([
    { role: 'ai', text: `مرحباً! أنا مساعدك الذكي لهذا الملف. يمكنني الإجابة على أسئلتك حول محتوى "${fileName}". بماذا تريد أن تعرف؟` },
  ])
  const [input, setInput]     = useState('')
  const [loading, setLoading] = useState(false)
  const bottomRef = useRef<HTMLDivElement>(null)

  useEffect(() => { bottomRef.current?.scrollIntoView({ behavior: 'smooth' }) }, [messages])

  const sendMessage = async () => {
    const text = input.trim()
    if (!text || loading) return
    setInput('')
    setMessages(prev => [...prev, { role: 'user', text }])
    setLoading(true)

    await new Promise(r => setTimeout(r, 1200))

    // Demo response
    const demoReplies: Record<string, string> = {
      default: 'بناءً على محتوى الملف، يمكنني القول أن هذا الموضوع يتعلق بتقنيات التخزين السحابي الحديثة. للمزيد من التفاصيل، يرجى توليد الترجمة أولاً.',
      وقت: 'يبدأ الحديث عن هذا الموضوع تقريباً في الدقيقة الثالثة من الملف.',
      ملخص: 'الفكرة الرئيسية للملف هي شرح منصة CloudVault AI وكيفية استخدام الذكاء الاصطناعي لتحسين تجربة التخزين السحابي.',
    }

    const reply = Object.entries(demoReplies).find(([k]) => text.includes(k))?.[1] ?? demoReplies.default
    setMessages(prev => [...prev, { role: 'ai', text: reply }])
    setLoading(false)
  }

  return (
    <div className="flex flex-col h-80">
      <div className="flex-1 overflow-y-auto space-y-3 mb-3">
        {messages.map((msg, i) => (
          <motion.div
            key={i}
            initial={{ opacity: 0, y: 6 }}
            animate={{ opacity: 1, y: 0 }}
            className={`flex gap-2 ${msg.role === 'user' ? 'flex-row-reverse' : 'flex-row'}`}
          >
            <div className={`w-7 h-7 rounded-full flex items-center justify-center flex-shrink-0 text-sm ${
              msg.role === 'ai'
                ? 'bg-gradient-to-br from-royal-500 to-violet-500'
                : 'bg-white/10'
            }`}>
              {msg.role === 'ai' ? <Cpu className="w-4 h-4 text-white" /> : '👤'}
            </div>
            <div className={`max-w-[80%] rounded-2xl px-3 py-2 text-sm leading-relaxed ${
              msg.role === 'ai'
                ? 'glass text-white/80'
                : 'bg-royal-500/20 text-white border border-royal-500/20'
            }`}>
              {msg.text}
            </div>
          </motion.div>
        ))}

        {loading && (
          <div className="flex gap-2">
            <div className="w-7 h-7 rounded-full bg-gradient-to-br from-royal-500 to-violet-500 flex items-center justify-center flex-shrink-0">
              <Cpu className="w-4 h-4 text-white" />
            </div>
            <div className="glass rounded-2xl px-4 py-3 flex items-center gap-1">
              {[0,1,2].map(i => (
                <div key={i} className="w-1.5 h-1.5 rounded-full bg-royal-400 animate-bounce" style={{ animationDelay: `${i * 0.15}s` }} />
              ))}
            </div>
          </div>
        )}
        <div ref={bottomRef} />
      </div>

      <div className="flex gap-2">
        <input
          value={input}
          onChange={e => setInput(e.target.value)}
          onKeyDown={e => e.key === 'Enter' && !e.shiftKey && sendMessage()}
          placeholder="اسأل عن محتوى الملف..."
          className="input-glass text-sm flex-1 py-2"
        />
        <motion.button
          whileTap={{ scale: 0.9 }}
          onClick={sendMessage} disabled={!input.trim() || loading}
          className="btn-primary p-2.5 disabled:opacity-50"
        >
          <Send className="w-4 h-4" />
        </motion.button>
      </div>
    </div>
  )
}

